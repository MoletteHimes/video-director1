export function inferPromptType(text: string, fallback: string) {
  if (/产品|卖点|品牌|购买|用户|广告|电商|口播|宣传/.test(text)) return "产品广告类";
  if (/宿舍|案发|民警|尸体|冰柜|警方|调查|证言|犯罪|刑侦|派出所|河里|白布|担架|水草|尸块|物证|记录本/.test(text)) {
    return "悬疑刑侦类";
  }
  if (/恐怖|悬疑|黑影|废弃|雨夜|死亡|旧照片|秘密|诡异/.test(text)) return "电影悬疑类";
  if (/主播|镜头前|讲述|台词|采访/.test(text)) return "口播人物类";
  if (/动漫|二次元|动画|anime/i.test(text)) return "动漫风格类";
  if (/小说|故事|剧情|人物|男人|女人|少年|女孩|村民|老板娘|警员|苏眉|包斩/.test(text)) return "小说剧情类";
  return fallback || "小说剧情类";
}

export function splitScriptIntoBeats(script: string) {
  const sentences = script
    .replace(/\r/g, "\n")
    .split(/[。！？；;?\n]+/)
    .map((part) => part.trim())
    .filter(Boolean);

  if (sentences.length >= 4) return sentences.slice(0, 12);
  if (sentences.length >= 2) return sentences;

  const text = script.trim();
  if (!text) return [];

  return [
    `建立场景与主体：${text}`,
    `放大关键线索或动作：${text}`,
    `人物反应与情绪推进：${text}`,
    `用稳定转场进入下一段：${text}`,
  ];
}

export function buildNegativePrompt(type: string) {
  const common =
    "不要文字，不要字幕，不要水印，不要 logo，不要人物脸崩坏，不要多余手指，不要多余肢体，不要画面撕裂，不要过曝，不要动作僵硬，不要人物身份混乱，不要服装突然变化，不要场景跳变，不要广告感，不要卡通化";

  if (/悬疑|刑侦|惊悚|犯罪/.test(type)) {
    return `${common}，不要怪物，不要鬼脸，不要突脸惊吓，不要大面积血腥，不要尸体正脸特写，不要伤口特写，不要器官，不要腐烂细节，不要夸张尖叫，不要喜剧表演，不要无关人群抢画面，不要把案件拍成犯罪教程`;
  }

  if (/产品|广告/.test(type)) {
    return `${common}，不要产品变形，不要材质错误，不要背景抢主体，不要错误商标，不要廉价塑料质感，不要手部畸形，不要功能演示混乱`;
  }

  return common;
}
